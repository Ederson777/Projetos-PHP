<h2>Banco de Dados</h2>
<h3>O que é Banco de Dados ou DATABASE ?</h3>
<p>Banco de dados é um servidor ou um local em seu servidor que é 
especifico para armazenar informações nele, que seja, lista de produtos, lista de usuários, em fim qualquer tipo de informaçõpes que vá usar ele, depois ela é armazenada em um banco de dados.</p>
<p>Por exemplo: Se tenho um Blogue, todas as postagem do meu blogue são armazenadas em um banco de dados. <br/><br/>
Se eu tenho uma Loja Virtual, Os produtos as vendas, os acessos, em fim, todas essas informações que eu vá usar em meu sistema, eu tenho que salvar em um Banco de Dados.

</p>

<h3>Como é que funciona um Banco de dados?</h3>
<p>Ele fica armazenado em um local específico do servidor, quando o PHP precisa de uma informação faz uma consulta a esse Banco de Dados, ou seja, ele envia uma série de comandos para o banco de dados, o Banco de Dados vai processar essas informações, e vai retornar a ele um resultados baseado nessa consulta
<br/>
Por exemplo: eu tenho lá, uma lista de usuários, uma lista de alunos por exemplo, então eu quero pegar todos os alunos que começam com a letra A<br/>
O PHP vai dizer
- ÓOO Banco de Dados, me de ai a lista de todos os alunos que começam com a letra A.<br/>
Então basicamente o Banco de Dados vai interpretar esse comando, e vai me retornar o resultado que eu quero. 
</p>
